    public int getTotal()
    {
        int total = 0;
        for (int i = 0; i < indice.length; i++)
        {
            total = total + indice[i];
        }
        return total;
    }

    public String getDiagnostico()
    {
        String resultado1;
        if (getTotal() >= 0 && getTotal() <= 30)
        {
            resultado1 = "No estrés";
        }
        else if (getTotal() >= 31 && getTotal() <= 42)
        {
            resultado1 = "Estrés leve";
        }
        else if (getTotal() >= 43 && getTotal() <= 54)
        {
            resultado1 = "Estrés medio";
        }
        else if (getTotal() >= 55 && getTotal() <= 66)
        {
            resultado1 = "Estrés alto";
        }
        else if (getTotal() >= 67 && getTotal() <= 72)
        {
            resultado1 = "Estres grave";
        }
        else
        {
            resultado1 = "Estrés fuera de rango";
        }

        return resultado1;
    }

