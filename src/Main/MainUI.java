    public void mostrarPregunta()
    {
        String sql;
        int valor;
        valor = contador;
        sql = "SELECT Pregunta FROM ExamenEstres WHERE id='" + valor + "'";
        try
        {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            if (rs.next())
            {
                jTextPanePregunta.setText(String.valueOf(contador + ".- ¿" + rs.getString("pregunta")));
                if (contador > 11)
                {
                    this.jButtonFinalizar.setVisible(true);
                    this.jButtonSiguiente.setEnabled(false);
                }
                else
                {
                    this.jButtonFinalizar.setVisible(false);
                    this.jButtonSiguiente.setEnabled(true);
                }
            }
        }
        catch (SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "No se realizó la conexión con éxito.", "ERROR FATAL", 0);
        }
    }

