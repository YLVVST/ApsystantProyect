    private void jButtonNuevoAspiranteActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButtonNuevoAspiranteActionPerformed
    {//GEN-HEADEREND:event_jButtonNuevoAspiranteActionPerformed
        try
        {
            Integer.parseInt(jTextFieldEdad.getText());
        }
        catch (NumberFormatException e)
        {
            JOptionPane.showMessageDialog(null, "ERROR: Ingrese una edad válida");
            jTextFieldEdad.requestFocus();
            jTextFieldEdad.selectAll();
        }
                
        if (jTextFieldNombre.getText().equals("") || !jTextFieldNombre.getText().matches("^[a-z A-ZÑñÁáÉéÍíÓóÚú']+$"))
        {
            JOptionPane.showMessageDialog(null, "ERROR: Ingrese un nombre válido");
            jTextFieldNombre.requestFocus();
            jTextFieldNombre.selectAll();
        }
        
        else if (jTextFieldEdad.getText().equals("") || Integer.valueOf(jTextFieldEdad.getText()) <= 5
                || Integer.valueOf(jTextFieldEdad.getText()) >= 95)
        {
            JOptionPane.showMessageDialog(null, "ERROR: Ingrese una edad válida");
            jTextFieldEdad.requestFocus();
            jTextFieldEdad.selectAll();
        }

        else if (jComboBoxSexo.getSelectedIndex() == 0)
        {
            JOptionPane.showMessageDialog(null, "ERROR: Seleccione un sexo");
            jComboBoxSexo.requestFocus();
            jComboBoxSexo.showPopup();
        }

        else
        {
            try
            {
                PreparedStatement pst = cn.prepareStatement("INSERT INTO Personas(nombre, edad, sexo, puntaje, diagnostico) VALUES (?,?,?,?,?)");
                pst.setString(1, jTextFieldNombre.getText());
                pst.setString(2, jTextFieldEdad.getText());
                pst.setString(3, String.valueOf(jComboBoxSexo.getSelectedItem()));
                pst.setString(4, String.valueOf(getTotal()));
                pst.setString(5, getDiagnostico());
                pst.executeUpdate();
            }
            catch (HeadlessException | SQLException e)
            {
                System.out.print(e.getMessage());
            }

            JOptionPane.showMessageDialog(null, "Aspirante registrado");
            stateLayoutUser(false);
            this.jLabelLogo.setVisible(false);
            this.jLabelLogoWaiting.setVisible(true);
            this.jButtonPlay.setVisible(true);
            nombre = jTextFieldNombre.getText();
            edad = jTextFieldEdad.getText();
            sexo = String.valueOf(jComboBoxSexo.getSelectedItem());
        }
    }//GEN-LAST:event_jButtonNuevoAspiranteActionPerformed

